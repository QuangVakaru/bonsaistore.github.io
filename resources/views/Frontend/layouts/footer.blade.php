      values: [ 100000, 10000000 ],
      slide: function( event, ui ) {
        $( "#amount" ).val( addPlus(ui.values[ 0 ]).toString() + "VNĐ" + " - " + addPlus(ui.values[ 1 ]).toString() + "VNĐ" );
        $( "#start_price" ).val( ui.values[ 0 ] );
